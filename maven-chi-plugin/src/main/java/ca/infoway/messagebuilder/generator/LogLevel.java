package ca.infoway.messagebuilder.generator;

/**
 * <p>The log level to write to.
 * 
 * @author <a href="http://www.intelliware.ca/">Intelliware Development</a>
 */
public enum LogLevel {
	
	/**
	 * <p>The log level for debug messages.</p>
	 */
	DEBUG, 
	/**
	 * <p>The log level for info messages.</p>
	 */
	INFO, 
	/**
	 * <p>The log level for warn messages.</p>
	 */
	WARN, 
	/**
	 * <p>The log level for error messages.</p>
	 */
	ERROR, 
	/**
	 * <p>The log level for severe messages.</p>
	 */
	SEVERE;

}
